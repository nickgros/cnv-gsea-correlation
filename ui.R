  mainPanel(
    tabsetPanel(
      tabPanel("Plot", plotOutput("plot")), 
      tabPanel("Summary", verbatimTextOutput("summary")), 
      tabPanel("Table", dataTableOutput("table"))
    )
  ),
  mainPanel(
    tags$a(href="http://nickgros.com/shiny.html", "nickgros.com")
